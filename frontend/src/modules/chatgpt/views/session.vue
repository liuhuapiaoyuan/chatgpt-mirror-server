<script lang="ts" name="chatgpt-session" setup>
import { useCrud, useTable, useUpsert } from "@cool-vue/crud";
import { useCool } from "/@/cool";

const { service } = useCool();

// cl-upsert 配置
const Upsert = useUpsert({
	items: [
		{ label: "邮箱", prop: "email", required: true, component: { name: "el-input" } },
		{ label: "密码", prop: "password", required: true, component: { name: "el-input" } },
		// { label: "用户ID", prop: "userID", required: true, component: { name: "el-input-number" } },
		{
			label: "状态",
			prop: "status",
			component: {
				name: "el-switch",
				props: {
					activeValue: 1,
					inactiveValue: 0
				}
			}
		},
		{
			label: "PLUS",
			prop: "isPlus",
			component: {
				name: "el-switch",
				props: {
					activeValue: 1,
					inactiveValue: 0
				}
			}
		},
		{
			label: "模式",
			prop: "mode",
			value: 0,
			component: {
				name: "el-radio-group",
				props: {},
				options: [
					{ label: "账号密码自动登录", value: 0 },
					{ label: "AccessToken手工刷新", value: 1 }
				]
			}
		},
		{
			label: "session",
			prop: "officialSession",
			component: {
				name: "el-input",
				props: {
					type: "textarea",
					rows: 4,
					placeholder: "如果是手工刷新模式，此处填写AccessToken"
				}
			}
		},
		{
			label: "账号备注",
			prop: "remark",
			component: { name: "el-input", props: { type: "textarea", rows: 4 } }
		}
	],
	onOpened(data) {
		// 自动生成uuid 作为userToken
		if (!data.userID) {
			data.userID = 0;
		}
		// localStorage.removeItem("arkoseToken");
		if (!data.officialSession) {
			console.log("onOpened---------->", data.officialSession);
			ElMessage({
				message: "请稍等,人机验证进行中.",
				type: "warning"
			});
			window.myEnforcement.run();
		}
	},
	onSubmit(data, { done, close, next }) {
		// 自动生成uuid 作为userToken
		let arkoseToken = localStorage.getItem("arkoseToken");
		let w = window;

		if (arkoseToken) {
			localStorage.removeItem("arkoseToken");

			next({ ...data, arkoseToken });
			done();
			close();
		} else {
			if (!data.officialSession) {
				w.myEnforcement.run();
				ElMessage({
					message: "请稍等,人机验证进行中,验证完成后请重新点击确定保存.",
					type: "warning"
				});
				// alert("请先完成人机验证");

				done();
			} else {
				next(data);
				done();
				close();
			}
		}
	}
});

// cl-table 配置
const Table = useTable({
	columns: [
		{ type: "selection" },
		{ label: "id", prop: "id" },
		{ label: "创建时间", prop: "createTime", sortable: true },
		{ label: "更新时间", prop: "updateTime", sortable: true },
		{ label: "邮箱", prop: "email", sortable: true },
		{ label: "密码", prop: "password", sortable: true },
		{
			label: "模式",
			prop: "mode",
			sortable: true,
			formatter(_row, _column, value) {
				return value == 0 ? "账号密码自动登录" : "AccessToken手工刷新";
			}
		},
		{ label: "状态", prop: "status", component: { name: "cl-switch" }, sortable: true },
		{ label: "PLUS", prop: "isPlus", component: { name: "cl-switch" }, sortable: true },
		{
			label: "session",
			prop: "officialSession",
			showOverflowTooltip: true,
			sortable: true
		},
		{
			label: "备注",
			prop: "remark",
			showOverflowTooltip: true,
			sortable: true
		},
		{ type: "op", buttons: ["edit", "delete"] }
	]
});

// cl-crud 配置
const Crud = useCrud(
	{
		service: service.chatgpt.session
	},
	(app) => {
		app.refresh();
	}
);
</script>
